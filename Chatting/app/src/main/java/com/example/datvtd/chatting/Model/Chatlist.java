package com.example.datvtd.chatting.Model;

public class Chatlist {
    public String id;
    public String color;

    public Chatlist() {
    }

    public Chatlist(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
